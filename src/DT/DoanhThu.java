
package DT;


public class DoanhThu {
    float doanhthu;
    int soluong;
    
    public DoanhThu(){
        doanhthu = 0;
        soluong = 0;
    }

    public float getDoanhthu() {
        return doanhthu;
    }

    public void setDoanhthu(float doanhthu) {
        this.doanhthu = doanhthu;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
    
    
}
