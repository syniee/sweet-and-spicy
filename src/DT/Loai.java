
package DT;


public class Loai {
    private String MaLoai, TenLoai;
    
    public Loai(){
        MaLoai = new String();
        TenLoai = new String();
    }

    public String getMaLoai() {
        return MaLoai;
    }

    public void setMaLoai(String MaLoai) {
        this.MaLoai = MaLoai;
    }

    public String getTenLoai() {
        return TenLoai;
    }

    public void setTenLoai(String TenLoai) {
        this.TenLoai = TenLoai;
    }
    
    
}
